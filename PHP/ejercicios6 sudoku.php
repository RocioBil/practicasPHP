function Varray($a)
{$i=0;
 $l= count($a);
    while ($i<($l-1))
    {
        $j=$i+1;
        while($j<$l)
        {
            If ($a [$i]==$a[$j])
            {
                return false;
            }
            $j++;
        }
        $i++;
    }
    return true;
}

function Vfilas($a)
{
    for ($i=0; $i< 9; $i++)
	{
        if (Varray($a[$i])== false)
        {
            return false;
        }
	}
	return true;
}

function Vcolumnas($a)
{
    for ($i=0; $i< 9; $i++)
    {
        $b = [];
